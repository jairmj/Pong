void Pelota::Mover(Barra* Jugador1, Barra* Jugador2) {
	Borrar();



	//Interacciones Jugador1
	if ((x + dx == 12) && (y >= Jugador1->retornar_y() && y <= Jugador1->retornar_y() + 4)) {
		dx = 1;// Está colisionando en la izquierda, por lo que siempre irá hacia la derecha
		if (y == Jugador1->retornar_y() || y == Jugador1->retornar_y() + 1) dy = -1;
		if (y == Jugador1->retornar_y() + 2) dy = dy;
		if (y == Jugador1->retornar_y() + 3 || y == Jugador1->retornar_y() + 4) dy = 1;
		Jugador1->cambiar_puntos(Jugador1->retornar_puntos() + 1);
		Jugador1->ActualizarPuntos();
	}
	else if ((x + dx == 12)) {
		Jugador2->cambiar_puntos(Jugador2->retornar_puntos() + 1);
		Jugador2->ActualizarPuntos();
		Borrar();//Borro la pelota
		Reset();
	}


	//Interacciones Jugador2
	if ((x + dx == 116) && (y >= Jugador2->retornar_y() && y <= Jugador2->retornar_y() + 4)) { //Si está en el x, y de la barra 2, rebota y da un punto
		dx = -1;// Está colisionando en la derecha, por lo que siempre irá hacia la izquierda
		if (y == Jugador2->retornar_y() || y == Jugador2->retornar_y() + 1) dy = -1; //Si le da con la parte de arriba, va hacia arriba
		if (y == Jugador2->retornar_y() + 2) dy = dy; //Si le da en el medio, sigue en la misma direccion
		if (y == Jugador2->retornar_y() + 3 || y == Jugador2->retornar_y() + 4) dy = 1; //Si le da con la parte de abajo, rebota hacia abajo
		Jugador2->cambiar_puntos(Jugador2->retornar_puntos() + 1); //Le ha dado, por lo que le doy un punto
		Jugador2->ActualizarPuntos();
	}

	else if ((x + dx == 117)) {//Si entra aquí es que el jugador 2 no le ha dado
		Jugador1->cambiar_puntos(Jugador1->retornar_puntos() + 1); //Le doy un punto al otro
		Jugador1->ActualizarPuntos();
		Borrar();//Borro la pelota
		Reset();
	}

	//Rebotes arriba y abajo
	if (y + dy <= 4 || y + dy >= 35) {
		dy *= -1;
	}
	//Movimiento
	x += dx;
	y += dy;

	Dibujar();

}

void Pelota::Reset() {
	x = 64;
	y = rand() % 30 + 5; // [5 - 34]
	dy = rand() % 3 - 1;//-1, 0, 1
	int posibilidadesX = rand() % 2; 
	switch (posibilidadesX)
	{
	case 0: dx = -1; break;
	case 1: dx = 1;
	}
}